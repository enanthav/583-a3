$("#btn06AB").click(function(){
    $(".G06grouprect").removeClass( "G06recton" );
    $("#G06grouprectAB").addClass( "G06recton" );
    $(".G06alluvial").removeClass( "idle" );
    $(this).addClass( "idle" );
    $('.G06flow').removeClass( "G06on" );
    $('#G06nodeAB').addClass( "G06on" );
});

